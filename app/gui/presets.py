"""
Document presets for the Agent-For-TOM GUI.

Each preset is a self-contained bundle that knows:
  * what to ask the user (its list of fields)
  * how to assemble a prompt for the AI from the user's input
  * how to convert the AI's response into the canonical
    `LabGuidelinesContent` schema used by the PDF/DOCX generators.

Adding a new preset is purely a matter of appending to `PRESETS` —
the GUI and the AI/render pipeline pick it up automatically.
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Callable

from app.backend.models import LabGuidelinesContent, LabWork


# ---------------------------------------------------------------------------
# Field definition
# ---------------------------------------------------------------------------

@dataclass
class Field:
    """A single form field shown in the GUI.

    `type` controls which widget the GUI uses to render it:
        * "text"     — single-line text input
        * "multiline" — multi-line text area
        * "list"     — dynamic list of one-line items (add/remove rows)
    """
    key: str
    label: str
    type: str = "text"
    default: str = ""
    required: bool = True
    help: str = ""


# ---------------------------------------------------------------------------
# Preset definition
# ---------------------------------------------------------------------------

@dataclass
class Preset:
    key: str
    name: str
    description: str
    fields: list[Field]
    build_system_prompt: Callable[[dict[str, Any], str], str]
    build_user_prompt: Callable[[dict[str, Any], str], str]
    fallback_content: Callable[[dict[str, Any], str], LabGuidelinesContent]


# ---------------------------------------------------------------------------
# Prompt helpers (shared)
# ---------------------------------------------------------------------------

def _system_prompt_base(persona: str, discipline: str) -> str:
    return (
        "Ви — досвідчений викладач українського університету (професор/доцент) "
        "з великим стажем нормоконтролю та методичної роботи.\n"
        f"Ваше завдання — написати детальний професійний контент для методичних "
        f"вказівок з дисципліни «{discipline}».\n\n"
        "Мова: виключно українська (академічний, науково-технічний стиль).\n"
        f"Стиль викладу: {persona}.\n"
        "  - formal_academic — строгий, безособовий, пасивний стан.\n"
        "  - practical_oriented — багато прикладів, фрагментів коду, практичних кроків.\n"
        "  - detailed_explanatory — для початківців, з аналогіями та розгорнутими поясненнями.\n"
        "  - concise_technical — короткі списки, мінімум опису, максимум фактів.\n\n"
        "Жодних плейсхолдерів типу «...», «TODO», «Lorem ipsum».\n"
        "Теоретична частина — 2-3 розгорнуті абзаци, пов'язані між собою.\n"
        "Контрольні питання — конкретні, без загальних «що таке?».\n"
        "Кожне посилання у полі references — у форматі ДСТУ 8302:2015 "
        "(Прізвище І. Б. Назва. Місто: Видавництво, Рік. Кількість с.).\n\n"
        "ВИ ВІДПОВІДАЄТЕ ВИКЛЮЧНО ВАЛІДНИМ JSON-ОБ'ЄКТОМ, без жодних пояснень поза JSON."
    )


def _json_contract() -> str:
    """The exact JSON shape the AI must return."""
    return (
        "Структура JSON (поверни САМЕ такий об'єкт, масив lab_works "
        "містить рівно стільки елементів, скільки потрібно):\n"
        "{\n"
        '  "introduction": "Короткий вступ (1-2 абзаци), що мотивує роботу. '
        'Поверни порожній рядок, якщо не потрібен.",\n'
        '  "lab_works": [\n'
        "    {\n"
        '      "topic": "Тема лабораторної роботи",\n'
        '      "objective": "Мета роботи — 1-2 речення",\n'
        '      "theory": "Загальні відомості — 2-3 розгорнуті абзаци",\n'
        '      "procedure": ["підготувати робоче середовище", "опрацювати теорію", '
        '"реалізувати програму", "протестувати", "оформити звіт"],\n'
        '      "questions": ["Питання 1?", "Питання 2?", "Питання 3?"],\n'
        '      "tasks": ["Завдання 1", "Завдання 2", "Завдання 3", "Завдання 4"],\n'
        '      "variants": ["Варіант 1", "Варіант 2", "Варіант 3", "Варіант 4", "Варіант 5"],\n'
        '      "report_sections": ["Титульний аркуш", "Мета роботи", "Хід роботи", '
        '"Результати", "Висновки"],\n'
        '      "references": ["Джерело 1 у форматі ДСТУ 8302:2015", "Джерело 2"]\n'
        "    }\n"
        "  ],\n"
        '  "references": []\n'
        "}\n\n"
        "ВАЖЛИВО:\n"
        "  - У масивах procedure, questions, tasks, variants, report_sections "
        "поверни ТІЛЬКИ зміст кожного пункту, БЕЗ префіксів «Крок N.», "
        "«1.», «1)», «-», «•» тощо. Генератор додасть нумерацію сам.\n"
        "  - Література (references) — 2-5 джерел у форматі ДСТУ 8302:2015.\n\n"
        "Поверни ТІЛЬКИ JSON. Жодного тексту до чи після. Жодних ```json``` огороджень."
    )


