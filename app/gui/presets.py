def _lab_work_system_prompt(values: dict, discipline: str) -> str:
    persona = values.get("_persona", "formal_academic")
    include_tv = bool(values.get("_include_tasks_variants", True))
    n_variants = int(values.get("_variant_count", 6) or 6)
    extra = ""
    if include_tv:
        extra = (
            "\nТакож згенеруйте розділи «Завдання» і «Варіанти»:\n"
            f"  - tasks — 4-8 конкретних ЗАВДАНЬ, які студент має виконати\n"
            f"  - variants — РІВНО {n_variants} ВАРІАНТІВ завдань (набори "
            "    номерів або короткі формулювання)\n"
        )
    else:
        extra = (
            "\nРозділи «Завдання» і «Варіанти» ВИМКНЕНО викладачем — поверніть "
            "порожні масиви tasks=[] та variants=[].\n"
        )
    return (
        _system_prompt_base(persona, discipline)
        + "\n\nЗараз ви пишете ОДНУ лабораторну роботу, яка друкується за "
        "структурою LMV (ДСТУ 3008:2015):\n"
        "  - topic, objective, theory — стандартні поля\n"
        "  - procedure — 4-7 КРОКІВ виконання (повертайте БЕЗ префіксів "
        "    «Крок N.» — генератор додасть нумерацію)\n"
        "  - questions — 5-7 контрольних питань\n"
        + extra
        + "  - report_sections — 4-6 пунктів ЗМІСТУ ЗВІТУ\n"
        "  - references — 3-5 джерел у форматі ДСТУ 8302:2015"
    )


def _lab_work_user_prompt(values: dict, discipline: str) -> str:
    include_tv = bool(values.get("_include_tasks_variants", True))
    n_variants = int(values.get("_variant_count", 6) or 6)
    tv_line = (
        f"Бажана кількість варіантів: {n_variants}\n" if include_tv else ""
    )
    return (
        "Згенеруй повний контент однієї лабораторної роботи з такими реквізитами:\n\n"
        f"Тема: {values.get('topic', '')}\n"
        f"Мета (побажання викладача): {values.get('objective_hint', '')}\n"
        f"Теоретичні акценти: {values.get('theory_hint', '')}\n"
        f"Бажана кількість кроків: {values.get('procedure_hint', '5')}\n"
        f"Бажана кількість контрольних питань: {values.get('questions_hint', '5')}\n"
        f"{tv_line}\n"
        + _json_contract()
    )


